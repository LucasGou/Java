package gestionvelo;

public class location {
String nom;
int jours;
boolean loue;
int type;
String couleurs;

public location(String nom, int jours, boolean loue, int type, String couleurs) {
	super();
	this.nom = nom;
	this.jours = jours;
	this.loue = loue;
	this.type = type;
	this.couleurs = couleurs;
}



}
